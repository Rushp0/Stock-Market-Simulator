	private void updateStockPortfolio() {

		String[][] portfolioData = new String[stockPortfolio.getPortfolioSize()][4];
		int i = 0;
		for (Stock key : stockPortfolio.getPortfolio().keySet()) {
			portfolioData[i] = stockPortfolio.getDataArray(key);
			i++;
		}

		DefaultTableModel table = (DefaultTableModel) stockPortfolioTable.getModel();
		table.setRowCount(0);

		for (String[] row : portfolioData)
			table.addRow(row);

		stockPortfolio.updateStockPortfolio();
		this.updateBankAccount();

		this.PortfolioBalanceLabel.setText(stockPortfolio.getPortfolioValueFormatted());
		this.PortfolioBalanceLabel
				.setMinimumSize(new java.awt.Dimension(this.PortfolioBalanceLabel.getText().length() * 36, 71));
	}

	@SuppressWarnings("deprecation")
	private void SidebarSellStocksBoxClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_SidebarSellStocksBoxClicked

		String sellingSymbol = JOptionPane.showInputDialog("Enter Stock Symbol to Sell: ");

		if (sellingSymbol == null) {
			JOptionPane.showMessageDialog(this, "Selling Symbol invalid.");
			return;
		}

		Stock s = new Stock(sellingSymbol);
		String amount = JOptionPane.showInputDialog(s.getSymbol() + "\n" + "Current Price: " + s.getPrice() + "\n"
				+ "Stocks Owned: " + stockPortfolio.getStockAmountOwned(s) + "\n" + "Enter amount to sell: ");

		boolean sold = false;
		if (!(sellingSymbol == null || amount == null))
			sold = stockPortfolio.sellStock(s, new Integer(amount));
		if (sold) {
			this.updateBankAccount();
			this.updateStockPortfolio();
		} else {
			JOptionPane.showMessageDialog(this, "Amount is invalid.");
		}

	}// GEN-LAST:event_SidebarSellStocksBoxClicked

