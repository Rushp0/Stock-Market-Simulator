	private void initAccounts() {
		// deserailize stock portfolio and bank account
		stockPortfolio.deserializePortfolio();

		updatePopularStocks();

		// init account value box
		this.AccountValueLabel.setText(stockPortfolio.getBankAccount().getAccountBalanceFormatted());
		this.AccountValueLabel
				.setMinimumSize(new java.awt.Dimension(this.AccountValueLabel.getText().length() * 36, 71));

		// init portfolio value box
		this.PortfolioBalanceLabel.setText(stockPortfolio.getPortfolioValueFormatted());
		this.PortfolioBalanceLabel
				.setMinimumSize(new java.awt.Dimension(this.PortfolioBalanceLabel.getText().length() * 36, 71));

		/**
		 * Adding stock portfolio to table:
		 */

		String[][] portfolioData = new String[stockPortfolio.getPortfolioSize()][4];
		int i = 0;
		for (Stock key : stockPortfolio.getPortfolio().keySet()) {
			portfolioData[i] = stockPortfolio.getDataArray(key);
			i++;
		}

		DefaultTableModel table = (DefaultTableModel) stockPortfolioTable.getModel();
		table.setRowCount(0);

		for (String[] row : portfolioData)
			table.addRow(row);

	}

