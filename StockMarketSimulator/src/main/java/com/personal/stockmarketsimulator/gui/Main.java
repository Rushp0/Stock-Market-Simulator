	@SuppressWarnings("deprecation")
	private void SearchEnterPressed(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_SearchEnterPressed

		if (evt.getKeyChar() == '\n') {
			final String[] OPTIONS = new String[] { "Close", "Purchase Stocks" };

			Stock s = new Stock(stockSearchField.getText());

			String stockSummary = "[" + s.getSymbol().toUpperCase() + "]" + "\n" + "Current Price: "
					+ s.getPriceFormatted() + "\n" + "Previous Close: " + s.getPreviousCloseFormatted() + "\n"
					+ "Volume: " + s.getVolume() + "\n" + "Open: $" + s.getOpen();

			int choice = JOptionPane.showOptionDialog(this, stockSummary, s.getSymbol() + " Stock Summary",
					JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, OPTIONS, OPTIONS[0]);

			// if the user chooses to purchase stocks
			if (OPTIONS[choice].equals("Purchase Stocks")) {

				String purchaseAmount = JOptionPane.showInputDialog("Enter number of stocks to purchase: ");
				if (purchaseAmount == null)
					purchaseAmount = null;

				while (purchaseAmount.isBlank() || purchaseAmount.isEmpty()) {
					purchaseAmount = JOptionPane.showInputDialog("Enter number of stocks to purchase: ");
					if (purchaseAmount == null)
						break;
				}

				stockPortfolio.purchaseStock(s, new Integer(purchaseAmount));
				this.updateStockPortfolio();
			}

			this.requestFocus();
		}

	}// GEN-LAST:event_SearchEnterPressed

